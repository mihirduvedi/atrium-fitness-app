#!/usr/bin/env node
// Regenerates supabase/seed.sql from packages/engine/data/archetypes.json.
// Run after any data drop: node scripts/generate-exercise-seed.mjs
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const data = JSON.parse(readFileSync(join(root, 'packages/engine/data/archetypes.json'), 'utf8'));

const esc = (s) => s.replaceAll("'", "''");
const rows = Object.entries(data.exercise_catalog).map(
  ([id, ex]) =>
    `  ('${esc(id)}', null, '${esc(ex.name)}', '${esc(ex.pattern)}', '${esc(ex.equipment)}', ${ex.level})`,
);

const sql = `-- GENERATED by scripts/generate-exercise-seed.mjs from archetypes.json — do not edit by hand.
-- Catalog exercises: text PK = archetypes.json slug, owner_user_id null (global, read-only via RLS).
insert into public.exercises (id, owner_user_id, name, pattern, equipment, level)
values
${rows.join(',\n')}
on conflict (id) do update
  set name = excluded.name,
      pattern = excluded.pattern,
      equipment = excluded.equipment,
      level = excluded.level;
`;

writeFileSync(join(root, 'supabase/seed.sql'), sql);
console.log(`wrote supabase/seed.sql (${rows.length} exercises)`);
